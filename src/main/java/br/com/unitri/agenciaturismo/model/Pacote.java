package br.com.unitri.agenciaturismo.model;


public class Pacote {
	
	private int id;
	
	private String nomePacote;
	
	private String cidadeSaida;
	
	private String paisSaida;
	
	private String dataInicio;
	
	private String dataFim;

	private float valorPacote;
	
	private int quantidadePacotes;
	
	private int idDestino;
	
	private int idHotel;
	
	private int idCompanhia;
	
	private int idCliente;
	
	private int idFuncionario;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNomePacote() {
		return nomePacote;
	}

	public void setNomePacote(String nomePacote) {
		this.nomePacote = nomePacote;
	}

	public String getCidadeSaida() {
		return cidadeSaida;
	}

	public void setCidadeSaida(String cidadeSaida) {
		this.cidadeSaida = cidadeSaida;
	}

	public String getPaisSaida() {
		return paisSaida;
	}

	public void setPaisSaida(String paisSaida) {
		this.paisSaida = paisSaida;
	}

	public String getDataInicio() {
		return dataInicio;
	}

	public void setDataInicio(String dataInicio) {
		this.dataInicio = dataInicio;
	}

	public String getDataFim() {
		return dataFim;
	}

	public void setDataFim(String dataFim) {
		this.dataFim = dataFim;
	}

	public float getValorPacote() {
		return valorPacote;
	}

	public void setValorPacote(float valorPacote) {
		this.valorPacote = valorPacote;
	}

	public int getQuantidadePacotes() {
		return quantidadePacotes;
	}

	public void setQuantidadePacotes(int quantidadePacotes) {
		this.quantidadePacotes = quantidadePacotes;
	}

	public int getIdDestino() {
		return idDestino;
	}

	public void setIdDestino(int idDestino) {
		this.idDestino = idDestino;
	}

	public int getIdHotel() {
		return idHotel;
	}

	public void setIdHotel(int idHotel) {
		this.idHotel = idHotel;
	}

	public int getIdCompanhia() {
		return idCompanhia;
	}

	public void setIdCompanhia(int idCompanhia) {
		this.idCompanhia = idCompanhia;
	}

	public int getIdCliente() {
		return idCliente;
	}

	public void setIdCliente(int idCliente) {
		this.idCliente = idCliente;
	}

	public int getIdFuncionario() {
		return idFuncionario;
	}

	public void setIdFuncionario(int idFuncionario) {
		this.idFuncionario = idFuncionario;
	}
	
	
}
